package com.android.Samkoonhmi.util;

public   class TextAttribute {
	public final static short BOLD      = 0x0001;//粗体
    public final static short ITALIC    = 0x0002;//斜体
    public final static short FLICKER   = 0x0004;//闪烁
    public final static short UNDERLINE = 0x0008;//下划线
    
    public final static short CENTER    = 0x0010;//文本居中
    public final static short LEFT      = 0x0020;//文本左靠
    public final static short RIGHT     = 0x0040;//文本右靠
    
    public final static short NOSET     = 0x0000;//没有设置
    public final static short PASSWORD  = 0x0080;//文字显示密码
 
}
