package com.android.Samkoonhmi.model;

/**
 * 场景编号和唯一id
 */
public class SceneNumInfo {

	private int num;//场景编号，可以编辑
	private int sid;//场景唯一编号
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
}
