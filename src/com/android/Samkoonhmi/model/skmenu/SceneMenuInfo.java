package com.android.Samkoonhmi.model.skmenu;


public class SceneMenuInfo {

	private int id;       //画面id
	private String name;  //画面名字
	private String pic;   //画面缩略图

	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPic() {
		return pic;
	}
	public void setPic(String pic) {
		this.pic = pic;
	}

}
